unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int length;
	unsigned int index;

	length = 0;
	index = 0;
	while (src[length] != '\0')
		length++;
	if (size != 0)
	{
		while (src[index] != '\0' && index < (size - 1))
		{
			dest[index] = src[index];
			index++;
		}
		dest[index] = '\0';
	}
	return (length);
}
